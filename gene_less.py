import random

Snums_majc = {
    '0601': ['01', '02'],
    '0602': ['01', '02', '03'],
    '0603': ['01', '02', '03'],
    '0604': ['01'],
    '0605': ['01']
}
lesson_info = [[
                   "('C01', '2020', '2', '000000000001')",
                   "('C01', '2020', '2', '000000000002')"
               ],
               [
                   "('C02', '2020', '2', '000000000002')",
                   "('C02', '2020', '2', '000000000003')",
                   "('C02', '2020', '2', '000000000004')"
               ],
               [
                   "('C03', '2020', '2', '000000000005')",
                   "('C03', '2020', '2', '000000000006')"
               ],
               [
                   "('C04', '2020', '2', '000000000005')"
                ],
               [
                   "('C05', '2020', '1', '000000000001')",
                   "('C05', '2020', '1', '000000000007')"
               ],
               [
                   "('C06', '2020', '1', '000000000002')",
                   "('C06', '2020', '1', '000000000003')"
               ]]

for m in Snums_majc.keys():
    for c in Snums_majc[m]:
        for ll in lesson_info:
            if m != '0601' and ll == lesson_info[3]:
                continue
            if m == '0601' and ll == lesson_info[2]:
                continue
            if m == '0602' and ll == lesson_info[4]:
                continue
            if m == '0604' or m == '0605' and ll == lesson_info[1]:
                continue
            le = random.choice(ll)
            s = le[:1] + f"'{m}', " + f"'{c}', " + le[1:] + ','
            print(s)
